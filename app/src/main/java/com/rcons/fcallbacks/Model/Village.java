package com.rcons.fcallbacks.Model;

public class Village {
    private String villageID;
    private String villageName;
    private String unionConcilName;
    private String unionConcilID;
    private String tehsilName;
    private String tehsilID;

    public String getVillageID() {
        return villageID;
    }

    public void setVillageID(String villageID) {
        this.villageID = villageID;
    }

    public String getVillageName() {
        return villageName;
    }

    public void setVillageName(String villageName) {
        this.villageName = villageName;
    }

    public String getUnionConcilName() {
        return unionConcilName;
    }

    public void setUnionConcilName(String unionConcilName) {
        this.unionConcilName = unionConcilName;
    }

    public String getUnionConcilID() {
        return unionConcilID;
    }

    public void setUnionConcilID(String unionConcilID) {
        this.unionConcilID = unionConcilID;
    }

    public String getTehsilName() {
        return tehsilName;
    }

    public void setTehsilName(String tehsilName) {
        this.tehsilName = tehsilName;
    }

    public String getTehsilID() {
        return tehsilID;
    }

    public void setTehsilID(String tehsilID) {
        this.tehsilID = tehsilID;
    }
}