package com.rcons.fcallbacks.Summary;

public class summary_items {
  private String username;
  private String newCalls;
  private String pendingCalls;
  private String completedCalls;
  private String successfullCalls;

    public summary_items() {
        this.username = username;
        this.newCalls = newCalls;
        this.pendingCalls = pendingCalls;
        this.completedCalls = completedCalls;
        this.successfullCalls = successfullCalls;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getNewCalls() {
        return newCalls;
    }

    public void setNewCalls(String newCalls) {
        this.newCalls = newCalls;
    }

    public String getPendingCalls() {
        return pendingCalls;
    }

    public void setPendingCalls(String pendingCalls) {
        this.pendingCalls = pendingCalls;
    }

    public String getcompletedCallsCalls() {
        return completedCalls;
    }

    public void setcompletedCallslCalls(String completedCalls) {
        this.completedCalls = completedCalls;
    }

    public String getSuccessfullCalls() {
        return successfullCalls;
    }

    public void setSuccessfullCalls(String successfullCalls) {
        this.successfullCalls = successfullCalls;
    }
}
